<script>
// Viewport
var viewportWidth
var viewportWidthInner
var viewportHeight
var viewportDim
var viewportDevice
var dotWidth
var sectionHeight

export default {
  methods: {
    checkViewport () {

      if (viewportWidthInner) {
        var iW = viewportWidthInner
      } else {
        var iW = viewportWidth
      }

      if (viewportWidth < 667) {
        viewportDevice = 'mobile'
        dotWidth = Math.floor(iW / 8)
        sectionHeight = dotWidth*9.5
      } else {
        dotWidth = Math.floor(iW / 24)
        viewportDevice = 'desktop'
        sectionHeight = dotWidth*8
      }
      document.querySelector('body').classList.remove('viewport-device-desktop', 'viewport-device-tablet', 'viewport-device-mobile')
      document.querySelector('body').classList.add('viewport-device-' + viewportDevice)
      if (viewportDim > 2) {
        document.querySelector('body').classList.remove('viewport-dim-square', 'viewport-dim-landscape')
        document.querySelector('body').classList.add('viewport-dim-landscape')
      } else {
        document.querySelector('body').classList.remove('viewport-dim-square', 'viewport-dim-landscape')
        document.querySelector('body').classList.add('viewport-dim-square')
      }
    },
    resizeLogic () {

      viewportWidth = window.innerWidth
      viewportHeight = window.innerHeight
      viewportDim = viewportWidth / viewportHeight
      if (viewportWidth < 667) {
        viewportDevice = 'mobile'
      } else {
        viewportDevice = 'desktop'
      }
      if (viewportDevice == 'mobile') {
        viewportWidthInner = viewportWidth
      } else {
        viewportWidthInner = viewportWidth-(viewportWidth/24)
      }

      this.checkViewport()

      var section = document.getElementById('SectionOne');
      if (viewportDevice == 'mobile') {
        section.style.padding = '0px'
        section.style.height = sectionHeight+(dotWidth*0)-1+'px'
      } else {
        section.style.height = sectionHeight+(dotWidth*2)-1+'px'
        section.style.padding = (dotWidth*0.5)+'px'
      }

      var SymbolBig = document.getElementById('SymbolBig');
      var SymbolBigHeight = dotWidth*3
      SymbolBig.style.height = SymbolBigHeight+4+'px'
      SymbolBig.style.width = SymbolBigHeight+4+'px'
      SymbolBig.style.left = (dotWidth*21)-1+'px'
      SymbolBig.style.top = (dotWidth*6)-1+'px'

      if (viewportDevice == 'mobile') {
        SymbolBig.style.height = (dotWidth*2)+4+'px'
        SymbolBig.style.width = (dotWidth*2)+4+'px'
        SymbolBig.style.top = (dotWidth*0.5)-0+'px'
        SymbolBig.style.left = (dotWidth*5.5)-1+'px'
      }

      var Nordix = document.getElementById('Nordix');
      Nordix.style.width = (dotWidth*6)+'px'
      Nordix.style.left = (dotWidth*0.82)+'px'
      Nordix.style.top = (dotWidth*2.68)+'px'

      var elements = document.getElementsByClassName('DotBound');
      for(var i = 0, length = elements.length; i < length; i++) {
        elements[i].style.height = dotWidth+'px'
        elements[i].style.width = dotWidth+'px'
      }
    },
  },
  mounted: function () {
    this.resizeLogic()
    // RESIZE
    window.addEventListener('resize', this.resizeLogic)
  },
}
</script>

